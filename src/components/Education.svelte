  <h2>¿Y en cuanto a mi educación?</h2>
  <p>Soy egresado del secundario en San Luis, rindiendo libre; aunque cursé toda mi secundaria en Pablo Nogués, la mejor escuela técnica de mi provincia.</p>
  <p>
    Inicié ingeniería electromecánica en Villa Mercedes, San Luis; y en el segundo año, paralelamente, me inscribí en la recién inaugurada facultad de Diseño Gráfico. Continué
    diseño por 2 años y luego seguí por mi cuenta, fuera de la facultad.
  </p>
  <p>
    De vuelta en Mendoza, mientras trabajaba, intenté formalizar mi educación varias veces. Terminé el tercer año de la carrera Contador Público Nacional, pero una vez más no
    encontré la motivación para continuar.
  </p>
  <p>Quizá lo único que me enorgullezca son mis cursos realizados en Platzi, que pueden ser consultados aquí: https://platzi.com/p/gcavedal/</p>
  <p>
    Lo cierto es que soy un apasionado por el conocimiento, devorador de libros y un <b>autodidacta patológico</b>. Tengo la mejor opinión sobre la educación formal, simplemente no
    es para mí.
  </p>
