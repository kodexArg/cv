<section>
  <h2><i class="fa-solid fa-clipboard-check" aria-hidden="true"></i>QA y Revisión de Proyectos</h2>

  <article>
    <p>
      En los últimos años profundicé fuertemente en aseguramiento de calidad aplicado al
      desarrollo asistido por IA: cuando los agentes escriben la mayor parte del código, la
      calidad se juega en las especificaciones, los contratos y la revisión — y ahí es donde
      trabajo.
    </p>
    <p>
      Mi método: especificaciones y ADRs como fuente única de verdad, APIs contract-first con
      cobertura verificada por tests, BDD/TDD y pipelines multi-agente de auditoría (modelos en
      jerarquía Opus/Sonnet/Haiku) que revisan código, specs y documentación de forma cruzada.
      Aplico el mismo proceso como revisor externo de proyectos de terceros: auditoría de
      arquitectura, code review y evaluación de deuda técnica.
    </p>
    <p>
      El caso más completo es <strong>Coveris</strong>, un SaaS de planificación de capacidad
      hospitalaria: el diseño arquitectónico es mío, pero la lógica de negocio me fue provista
      por médicos. Trabajé directamente con profesionales clínicos traduciendo sus reglas de
      dominio a especificaciones verificables y trazables (más de 25 ADRs), con QA automatizado
      validando cada contrato contra esas reglas. Es el patrón que me interesa repetir: dominio
      experto ajeno + arquitectura y calidad propias.
    </p>
  </article>
</section>
