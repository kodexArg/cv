  <h2>Acerca de mí</h2>
  <article>

    <p>
      Me desempeño como Líder Técnico con manejo de la mayoría de operaciones de TI para un grupo variado de empresas, con unidades de negocio de diferentes dimensiones, visiones y presupuestos. Esta experiencia me otorga una ventaja para entender cómo se desarrollan los negocios desde adentro, identificar sus áreas críticas, comprender los procesos de toma de decisión y discernir qué necesitan y qué tecnologías pueden aprovechar para funcionar mejor.
    </p>

    <p>
      En mis 45 años he aprendido muchas cosas, pero por encima de todas valoro una: <strong>he aprendido a aprender</strong>. Soy desde siempre un autodidacta muy responsable, un lector voraz y he alcanzado un nivel alto en muchas de las habilidades que en esta currícula destaco. Necesito entender los temas a profundidad, me apasiona descubrir y sorprenderme, y tengo una muy marcada tendencia a explorar nuevas tecnologías.
    </p>

    <p>
      Por estos días, mis pasiones no están en sintonía con la visión de la empresa en la que trabajo, a la que realmente aprecio mucho. Estoy en busca de un cambio radical que me sumerja en el ámbito de la programación, de la inteligencia artificial, con la que sigo maravillado y en permanente exploración, y otras tecnologías emergentes. Mi aspiración es que el diseño de aplicaciones y la computación en la nube se conviertan en las actividades centrales que definan tanto mi trabajo como mis intereses principales.
    </p>

    <p>
      He ordenado mis habilidades ponderando entre las que más me apasionan, en las que quiero crecer y en las que más conocimientos tengo para aportar.
    </p>

    <p>
      Estaré encantado de ser contactado por el medio que desees.
    </p>

  </article>
